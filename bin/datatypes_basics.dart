void main() {
  ///variables used to store values in the program
  /// to create a variable
  /// syntax => datatype varible_name = value
  String name = "DHIVYA";
  int age = 30;
  int phone = 0987654321;
  double cgpa = 8.1;
  String email_id = 'dhivya.kv1992@gmail.com';

