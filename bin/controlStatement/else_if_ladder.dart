void main()
{
  String size='XXL';
  if(size=='XS') {
    print('YOUR SHIRT SIZE IS XS');
  }
  else if (size == 'S') {
    print('YOUR SHIRT SIZE IS S');
  }
  else if (size == 'M') {
    print('YOUR SHIRT SIZE IS M');
  }
  else if (size == 'L') {
    print('YOUR SHIRT SIZE IS L');
  }
  else if (size == 'XL') {
    print('YOUR SHIRT SIZE IS XL');
  }
else
  {
  print('YOUR SHIRT SIZE IS NOT AVAILABLE');
  }
}